export function getStoreById(id: string): Store | undefined {
  return stores.find(s => s.id === id);
}

export function getStoreBySlug(slug: string): Store | undefined {
  return stores.find(s => s.slug === slug);
}

export function getDealById(id: string): Deal | undefined {
  return deals.find(d => d.id === id);
}

export function getDealsByStore(storeId: string): Deal[] {
  return deals.filter(d => d.storeId === storeId);
}

export function getStoresByCategory(categorySlug: string): Store[] {
  if (categorySlug === 'all') return stores;
  return stores.filter(s => s.category?.slug === categorySlug);
}

export function getFeaturedStores(): Store[] {
  return stores.filter(s => s.isFeatured);
}

export function getHotDeals(): Deal[] {
  return deals.slice(0, 8);
}

export function searchStores(query: string): Store[] {
  const lower = query.toLowerCase();
  return stores.filter(s =>
    s.name.toLowerCase().includes(lower) ||
    s.description.toLowerCase().includes(lower) ||
    s.category?.name.toLowerCase().includes(lower)
  );
}
